package sy01;

public class student {
    public String stuNum;
    public String name;
    public String sex;
    public String telNum;
    public String address;

    public student(String stuNum, String name, String sex, String telNum, String address) {
        this.stuNum = stuNum;
        this.name = name;
        this.sex = sex;
        this.telNum = telNum;
        this.address = address;
    }


}
